// What is array?
// It is Ordered collection of object which store multiple values in it.
// Object is used to store key-value pairs.

// For Loops --->
// Print table of 2
for(let i = 2; i <= 20; i = i + 2)
    console.log(i)

// print table of 2 in reverse 
for(let i1 = 20; i1 >= 2; i1 = i1 -2)
    console.log(i1)

// While loops --->
// print table of 2
let i2 = 2
while(i2 <= 20){
    i2 = i2 + 2
    console.log(i2)
}

// print table of 2 in reverse
let i3 = 20
while(i3 >= 2){
    i3 = i3 - 2
    console.log(i3)
}

